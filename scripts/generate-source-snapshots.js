#!/usr/bin/env node
// Generates raw-text source snapshots so showcase pages can render their own
// implementation in a Code tab without runtime fetches or fragile ?raw imports.
const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');

const TARGETS = [
  {
    sources: [
      { id: 'fullChatHtml', path: 'src/app/pages/full-chat.html' },
    ],
    out: 'src/app/pages/full-chat.source.ts',
  },
];

function escape(text) {
  return text.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

function constName(id) {
  return id.replace(/[A-Z]/g, (m) => '_' + m).toUpperCase() + '_SOURCE';
}

for (const target of TARGETS) {
  const lines = [
    '// AUTO-GENERATED by scripts/generate-source-snapshots.js — do not edit by hand.',
    '// Re-run `bun run gen:sources` after editing the linked source files.',
    '',
  ];
  for (const s of TARGETS[0].sources) {
    const abs = path.join(ROOT, s.path);
    const text = fs.readFileSync(abs, 'utf8');
    lines.push(`export const ${constName(s.id)} = \`${escape(text)}\`;`);
    lines.push('');
  }
  const outPath = path.join(ROOT, target.out);
  fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
  console.log('wrote', target.out);
}
